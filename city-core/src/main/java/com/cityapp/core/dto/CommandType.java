package com.cityapp.core.dto;

/**
 * Перечисление типов команд для клиент-серверного взаимодействия.
 * <p>Каждая константа соответствует одной команде, доступной
 * для выполнения в приложении.</p>
 *
 * <p>Команды делятся на категории:
 * <ul>
 *   <li><b>Модификация коллекции:</b> ADD, UPDATE, REMOVE_BY_ID, CLEAR</li>
 *   <li><b>Получение информации:</b> SHOW, INFO, GET_BY_ID</li>
 *   <li><b>Фильтрация и агрегация:</b> FILTER_CONTAINS_NAME, MAX_BY_DENSITY, COUNT_BY_GOVERNMENT</li>
 *   <li><b>Системные:</b> SAVE (только сервер), EXIT (только клиент), HELP</li>
 * </ul>
 * </p>
 *
 * @author Алина
 * @version 1.0
 */
public enum CommandType {

    // === Модификация коллекции ===

    /**
     * Добавить новый элемент в коллекцию.
     * <p>Требует объект City в поле payload.</p>
     */
    ADD,

    /**
     * Обновить существующий элемент по ID.
     * <p>Требует объект City с заполненным id в поле payload.</p>
     */
    UPDATE,

    /**
     * Удалить элемент по уникальному идентификатору.
     * <p>Требует Integer (id) в поле payload.</p>
     */
    REMOVE_BY_ID,

    /**
     * Очистить всю коллекцию.
     * <p>Поле payload не используется.</p>
     */
    CLEAR,


    // === Получение информации ===

    /**
     * Вывести все элементы коллекции (отсортированные по имени).
     * <p>Поле payload не используется.</p>
     */
    SHOW,

    /**
     * Вывести информацию о коллекции (тип, дата инициализации, размер).
     * <p>Поле payload не используется.</p>
     */
    INFO,

    /**
     * Получить элемент по уникальному идентификатору.
     * <p>Требует Integer (id) в поле payload.</p>
     */
    GET_BY_ID,


    // === Фильтрация и агрегация (Stream API) ===

    /**
     * Фильтрация коллекции по подстроке в названии.
     * <p>Требует String (подстрока) в поле payload.</p>
     */
    FILTER_CONTAINS_NAME,

    /**
     * Найти элемент с максимальным значением плотности населения.
     * <p>Поле payload не используется.</p>
     */
    MAX_BY_DENSITY,

    /**
     * Подсчитать количество элементов, чьё правительство больше заданного.
     * <p>Требует Government в поле payload.</p>
     */
    COUNT_BY_GOVERNMENT,


    // === Системные команды ===

    /**
     * Сохранить коллекцию в файл.
     * <p><b>Доступно только на сервере!</b> Клиент не может отправить эту команду.</p>
     */
    SAVE,

    /**
     * Завершить работу клиентского приложения.
     * <p><b>Доступно только на клиенте!</b> Не отправляется на сервер.</p>
     */
    EXIT,

    /**
     * Вывести справку по доступным командам.
     * <p>Поле payload не используется.</p>
     */
    HELP
}